         'Point: %f\n', ...
         'Step size: %f\n', ...
         'Expected: %d\n', ...
         'Actual: %d\n', ...
         'Tolerance: %e\n'], ...
        func2str(f), x, h, expect, actual, tolerance);
    fprintf('Error Estimate: %f\n', error_estimate);
end

function test_CrossCheck()
    f = @(x) 2*x + 3;
    x = 5;
    h = 0.1;
    [actual_stencil_derivative, error_estimate] = FivePoint_Stencil(f, x, h);
    actual_derivative = 2;
    tolerance = 1e-6;
    assert(abs(actual_stencil_derivative - actual_derivative) < tolerance, ...
        ['Test case CrossCheck failed:\n', ...
         'Function: %s\n', ...
         'Point: %f\n', ...
         'Step size: %f\n', ...
         'Five-Point Stencil Derivative: %d\n', ...
         'Actual Derivative: %d\n', ...
         'Tolerance: %e\n'], ...
        func2str(f), x, h, actual_stencil_derivative, actual_derivative, tolerance);
    fprintf('Error Estimate: %f\n', error_estimate);
end
