function test_CrossCheck()
    f = @(x) 2*x + 3; 
    h = 0.1;
    x = 5;
    actual_SymDiff_quotient = SymDiff_quotient(f, x, h);
    actual_derivative = 2;  
    tolerance = 1e-5;
    assert(abs(actual_SymDiff_quotient - actual_derivative) < tolerance, ...
        ['Test case CrossCheck failed:\n', ...
         'Function: %s\n', ...
         'Point: %f\n', ...
         'Step size: %f\n', ...
         'Symmetrical Difference Quotient: %f\n', ...
         'Actual Derivative: %f\n', ...
         'Tolerance: %e\n'], ...
        func2str(f), x, h, actual_SymDiff_quotient, actual_derivative, tolerance);
end

