end

function test_symmetry()
    f = @(x) x.^3;
    h = 0.1;
    x = -2;
    actual_forward = SymDiff_quotient(f, x, h);
    actual_backward = SymDiff_quotient(f, x, -h);
    tolerance = 0.1;
    assert(abs(actual_forward - actual_backward) < tolerance, ...
        ['Test case symmetry failed:\n', ...
         'Function: %s\n', ...
         'Point: %f\n', ...
         'Step size: %f\n', ...
         'Forward Symmetrical Difference Quotient: %f\n', ...
         'Backward Symmetrical Difference Quotient: %f\n', ...
         'Tolerance: %e\n'], ...
        func2str(f), x, h, actual_forward, actual_backward, tolerance);
end


