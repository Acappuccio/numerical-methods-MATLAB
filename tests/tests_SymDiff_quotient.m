function tests_SymDiff_quotient()
    tests = {@test_trivial, @test_precision, @test_symmetry, @test_limiting, @test_CrossCheck};
    for t = tests
        try
            feval(t{:});
        catch E
            fprintf(2, 'Failed %s\n %s\n', func2str(t{:}), E.message)
        end
    end
end

function test_trivial()
    f = @(x) 5;  
    h = 0.1;
    x = 2;
    actual = SymDiff_quotient(f, x, h);
    expect = 0;  
    tolerance = 1e-5;
    assert(abs(expect - actual) < tolerance, ...
        ['Test case trivial failed:\n', ...
         'Function: %s\n', ...
         'Point: %f\n', ...
         'Step size: %f\n', ...
         'Expected: %f\n', ...
         'Actual: %f\n', ...
         'Tolerance: %e\n'], ...
        func2str(f), x, h, expect, actual, tolerance);
end

function test_precision()
    f = @(x) x.^2;
