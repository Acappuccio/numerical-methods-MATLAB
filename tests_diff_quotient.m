function test_CrossCheck()
    f = @(x) 2*x + 3;
    h = 0.1;
    x = 5;
    actual_diff_quotient = diff_quotient(f, x, h);
    actual_derivative = 2;
    tolerance = 1e-5;
    assert(abs(actual_diff_quotient - actual_derivative) < tolerance, ...
        ['Test case CrossCheck failed:\n', ...
         'Function: %s\n', ...
         'Point: %f\n', ...
         'Step size: %f\n', ...
         'Difference Quotient: %f\n', ...
         'Actual Derivative: %f\n', ...
         'Tolerance: %e\n'], ...
        func2str(f), x, h, actual_diff_quotient, actual_derivative, tolerance);
end