end

function test_symmetry()
    f = @(x) x.^3;
    h = 0.01;
    x = -2;
    actual_forward = diff_quotient(f, x, h);
    actual_backward = -1*(diff_quotient(f, x, h));
    tolerance = 1e-5;
    assert(abs(actual_forward + actual_backward) < tolerance, ...
        ['Test case symmetry failed:\n', ...
         'Function: %s\n', ...
         'Point: %f\n', ...
         'Step size: %f\n', ...
         'Forward Difference Quotient: %f\n', ...
         'Backward Difference Quotient: %f\n', ...
         'Tolerance: %e\n'], ...
        func2str(f), x, h, actual_forward, actual_backward, tolerance);
end

